# The C_min value for S^2 from the paper (gap between S=0 and S=1/2) is 3/4. 
# Therefore, C_min^2 = (0.75)^2 = 0.5625
C_MIN_SQUARED = 0.5625

print(f"{'Molecule':<15} | {'Singlet (Ha)':<15} | {'Triplet (Ha)':<15} | {'Gap (Ha)':<10} | {'Calculated Beta'}")
print("-" * 80)

for name, geom in molecules.items():
    
    try:
        # --- 1. Compute Singlet Ground State (S=0) ---
        # PySCF spin parameter = number of unpaired electrons. Singlet = 0.
        mol_singlet = gto.M(atom=geom, basis="6-31g*", spin=0, charge=0, verbose=0)
        mf_singlet = dft.RKS(mol_singlet)
        mf_singlet.xc = 'cam-b3lyp'  # You can use your custom xcfun string here if preferred
        e_singlet = mf_singlet.kernel()
        
        # --- 2. Compute Triplet State (S=1) ---
        # S=1 means 2 unpaired electrons, so spin=2 in PySCF
        mol_triplet = gto.M(atom=geom, basis="6-31g*", spin=2, charge=0, verbose=0)
        mf_triplet = dft.UKS(mol_triplet) # Unrestricted Kohn-Sham for open-shell triplet
        mf_triplet.xc = 'cam-b3lyp'
        e_triplet = mf_triplet.kernel()
        
        # --- 3. Calculate Gap and Beta ---
        delta_E = e_triplet - e_singlet
        
        # Kuroiwa and Nakagawa Formula (14)
        beta_ce = delta_E / C_MIN_SQUARED
        
        print(f"{name:<15} | {e_singlet:>15.5f} | {e_triplet:>15.5f} | {delta_E:>10.5f} | {beta_ce:.5f}")
        
    except Exception as e:
        print(f"{name:<15} | Error: {e}")

print("-" * 80)